
package ventana;


public class Ventana {
    String minimizar;
    String maximizar;
    String color;
    String cerrar;
    int tamañoX;
    int tamañoY;

    public Ventana() {
        
    }

    public String getMinimizar() {
        return minimizar;
    }

    public void setMinimizar(String minimizar) {
        this.minimizar = minimizar;
    }

    public String getMaximizar() {
        return maximizar;
    }

    public void setMaximizar(String maximizar) {
        this.maximizar = maximizar;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getCerrar() {
        return cerrar;
    }

    public void setCerrar(String cerrar) {
        this.cerrar = cerrar;
    }

    public int getTamañoX() {
        return tamañoX;
    }

    public void setTamañoX(int tamañoX) {
        this.tamañoX = tamañoX;
    }

    public int getTamañoY() {
        return tamañoY;
    }

    public void setTamañoY(int tamañoY) {
        this.tamañoY = tamañoY;
    }

    
}
